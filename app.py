import streamlit as st
import numpy as np
from PIL import Image
import joblib
import time

# ==============================
# Page Configuration
# ==============================
st.set_page_config(
    page_title="Male vs Female Classifier",
    page_icon="👨‍💻",
    layout="wide"
)

# ==============================
# Sidebar
# ==============================
with st.sidebar:
    st.title("📋 Project Information")

    dark_mode = st.toggle("🌙 Dark Mode")

    st.markdown("---")

    st.success("🤖 Model")
    st.write("Logistic Regression")

    st.markdown("### 📂 Classes")
    st.write("👩 Female")
    st.write("👨 Male")

    st.markdown("---")

    st.markdown("### 📸 Image Requirements")
    st.write("✔ JPG / JPEG / PNG")
    st.write("✔ Clear face image")
    st.write("✔ Front-facing face")
    st.write("✔ RGB image")

    st.markdown("---")

    st.info(
        "Upload a face image and the model predicts whether it belongs to a Male or Female."
    )

# ==============================
# Theme
# ==============================
if dark_mode:
    bg = "#0E1117"
    card = "#262730"
    text = "white"
else:
    bg = "#F5F7FA"
    card = "white"
    text = "black"

st.markdown(f"""
<style>

.stApp{{
background:{bg};
color:{text};
}}

.card{{
background:{card};
padding:20px;
border-radius:15px;
box-shadow:0px 3px 10px rgba(0,0,0,0.15);
}}

</style>
""", unsafe_allow_html=True)

# ==============================
# Load Model
# ==============================
model = joblib.load("male_female_model.pkl")
IMG_SIZE = 64

# ==============================
# Header
# ==============================
st.markdown("""
<h1 style='text-align:center;'>
👨👩 Male vs Female Face Classifier
</h1>
""", unsafe_allow_html=True)

st.caption(
    "Artificial Intelligence based Gender Classification using Machine Learning"
)

st.info("📤 Upload a clear front-facing face image to begin prediction.")

# ==============================
# Upload Image
# ==============================
uploaded_file = st.file_uploader(
    "Choose Image",
    type=["jpg", "jpeg", "png"]
)

# ==============================
# Prediction
# ==============================
if uploaded_file:

    image = Image.open(uploaded_file).convert("RGB")

    col1, col2 = st.columns([1, 1])

    with col1:

        st.image(
            image,
            caption="Uploaded Image",
            use_container_width=True
        )

    img = image.resize((IMG_SIZE, IMG_SIZE))
    img = np.array(img).flatten()

    with st.spinner("🤖 AI is analyzing the image..."):
        time.sleep(1.5)

        prediction = model.predict([img])[0]
        probability = model.predict_proba([img])[0]

    if prediction == 0:
        result = "👩 Female"
        confidence = probability[0] * 100
    else:
        result = "👨 Male"
        confidence = probability[1] * 100

    with col2:

        st.markdown("## 🎯 Prediction")

        st.success(result)

        st.metric(
            "Model Confidence",
            f"{confidence:.2f}%"
        )

        st.toast("Prediction Completed ✅", icon="🎉")

        report = f"""
Male vs Female Prediction Report

Prediction : {result}

Confidence : {confidence:.2f}%
"""

        st.download_button(
            "📥 Download Prediction Report",
            report,
            file_name="Prediction_Report.txt"
        )

# ==============================
# About
# ==============================
with st.expander("ℹ️ How does it work?"):

    st.write("""
### Workflow

1. Upload a face image.

2. The image is converted into RGB format.

3. It is resized to **64 × 64** pixels.

4. Pixel values are converted into numerical features.

5. Logistic Regression predicts the gender.

6. The prediction and confidence score are displayed.
""")

